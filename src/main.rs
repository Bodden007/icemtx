//! Главный модуль приложения IceMTX.
//!
//! Этот модуль содержит точку входа в программу и отвечает за запуск основного приложения.
//!
//! # Пример использования
//!
//! Приложение запускается автоматически при выполнении бинарного файла:
//!
//! ```bash
//! cargo run
//! ```
//!
//! # Структура
//!
//! - `mod app` - подключает модуль приложения
//! - `use app::run as app_run` - импортирует функцию запуска приложения
//!
//! # Примечания
//!
//! Приложение предназначено для работы с протоколом FLECS через последовательный порт.

mod app;
use app::run as app_run;

/// Точка входа в программу.
///
/// Запускает основную логику приложения через вызов функции `app_run()`.
///
/// # Действия
///
/// 1. Подключает модуль приложения
/// 2. Вызывает функцию `app_run()` из модуля `app`
/// 3. Завершает работу при завершении `app_run()`
///
/// # Обработка ошибок
///
/// Все ошибки обрабатываются внутри `app_run()`. Если происходит критическая ошибка,
/// приложение завершится с соответствующим сообщением об ошибке.
fn main() {
    app_run();
}
