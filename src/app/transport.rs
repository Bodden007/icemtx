//! Модуль транспорта для работы с внешними устройствами.
//!
//! Этот модуль предоставляет абстракции для различных типов транспорта,
//! используемых в приложении для обмена данными.
//!
//! # Подмодули
//!
//! - [`serial`] - реализация последовательного порта (RS-232/RS-485)
//!
//! # Использование
//!
//! Для работы с последовательным портом используйте структуру [`SerialSession`](serial::SerialSession)
//! из подмодуля `serial`.
//!
//! ```rust
//! use crate::app::transport::serial::SerialSession;
//!
//! let mut session = SerialSession::new("COM3", 9600).unwrap();
//! session.send(b"Hello").unwrap();
//! ```

