//! Форматирование полей FLECS для передачи.
//!
//! Этот модуль содержит функции для форматирования значений полей FLECS
//! в соответствии с требованиями протокола. Форматирование включает:
//! - Приведение чисел к заданной точности
//! - Добавление единиц измерения
//! - Выравнивание по ширине
//! - Добавление разделителей между полями
//!
//! # Основные функции
//!
//! - [`format_field()`] - форматирует одно поле с учётом его метаданных
//! - [`format_value_by_precision()`] - внутренняя функция для форматирования чисел

use super::FlecsField;

/// Количество пробелов после запятой-разделителя в CSV-формате.
pub const GAP_AFTER_COMMA: usize = 7;

/// Форматирует значение поля для передачи по протоколу FLECS.
///
/// Функция принимает поле и его сырое строковое значение, затем:
/// 1. Приводит число к требуемой точности (если это число)
/// 2. Добавляет единицу измерения (если она определена)
/// 3. Выравнивает результат по ширине поля
/// 4. Добавляет запятую-разделитель и пробелы после неё
///
/// # Аргументы
///
/// * `field` - поле FLECS, определяющее метаданные форматирования
/// * `value_raw` - сырое значение поля (например, "12.3", "0", "1.23e-4")
///
/// # Возвращает
///
/// Отформатированную строку, готовую для включения в FLECS-фрейм.
///
/// # Пример
///
/// ```rust
/// use crate::app::protocol::flecs::{FlecsField, format::format_field};
///
/// let formatted = format_field(FlecsField::RecircDensity, "12.345");
/// // Результат: "12.34 ppg      ,       "
/// ```
pub fn format_field(field: FlecsField, value_raw: &str) -> String {
    let meta = field.meta();

    let value_str = format_value_by_precision(value_raw, meta.precision);

    let core = match meta.unit {
        Some(unit) => format!("{value_str} {unit}"),
        None => value_str,
    };

    // Выравниваем вправо по ширине (ведущие пробелы)
    // В спецификации разрешены пробелы для выравнивания [0..20]
    let mut out = format!("{core:>width$},", width = meta.width);

    // "CSV между значениями отступ 7 пробелов"
    out.push_str(&" ".repeat(GAP_AFTER_COMMA));
    out
}

/// Форматирует числовое значение с заданной точностью.
///
/// Если входная строка может быть преобразована в число с плавающей точкой,
/// функция форматирует его с указанным количеством знаков после запятой.
/// В противном случае возвращает исходную строку без изменений.
///
/// # Аргументы
///
/// * `value_raw` - сырое строковое значение
/// * `precision` - требуемая точность (0-3 знака после запятой)
///
/// # Возвращает
///
/// Отформатированную строку.
fn format_value_by_precision(value_raw: &str, precision: u8) -> String {
    // Если это число — форматируем.
    // Если не число (пусто/мусор) — возвращаем как есть.
    if let Ok(v) = value_raw.trim().parse::<f64>() {
        match precision {
            0 => format!("{:.0}", v),
            1 => format!("{:.1}", v),
            2 => format!("{:.2}", v),
            3 => format!("{:.3}", v),
            _ => value_raw.to_string(),
        }
    } else {
        value_raw.to_string()
    }
}
